// newline character
"Hello\nWorld";
// 'Hello\nWorld'

console.log("Hello\nWorld");
// Hello
// World

// some of the escape sequences (same in Python):
// Escape sequence          Output
// \'                       single quote
// \"                       double quote
// \\                       backslash
// \n                       newline
// \t                       tab

console.log("This string has \"double quotes\" and a \\ back slash character");
// This string has "double quotes" and a \ back slash character

console.log("You don't need to escape single quotes ''"); // same in Python
// You don't need to escape single quotes ''

console.log('You don\'t need to escape double quotes ""'); // same in Python
// You don't need to escape double quotes ""
